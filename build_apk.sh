#!/bin/bash

# ==============================================================================
# نص أتمتة وتجميع تطبيق القرآن الكريم للأندرويد
# Quran Kareem Android Gradle & Web Assets Synchronization Script
# ==============================================================================

# تلوين مخرجات النصوص بشكل احترافي
GREEN='\033[0;32m'
GOLD='\033[1;33m'
CYAN='\033[0;36m'
RED='\033[0;31m'
NC='\033[0m' # No Color

echo -e "${GOLD}======================================================"
echo -e "   جاري تجميع وتحويل تطبيق القرآن الكريم إلى أندرويد   "
echo -e "======================================================${NC}"

# الخطوة 1: بناء وتجميع تطبيق الويب React + Vite
echo -e "\n${CYAN}[1/3] جاري تجميع كود الويب React + Vite...${NC}"
if [ -f "package.json" ]; then
    npm run build
    if [ $? -eq 0 ]; then
        echo -e "${GREEN}✓ تم تجميع ملفات الويب بنجاح داخل مجلد 'dist/'${NC}"
    else
        echo -e "${RED}✗ فشل تجميع تطبيق الويب. يرجى التحقق من الأخطاء البرمجية.${NC}"
        exit 1
    fi
else
    echo -e "${RED}✗ لم يتم العثور على ملف package.json في المسار الحالي.${NC}"
    exit 1
fi

# الخطوة 2: تحديث مشروع الأندرويد باستخدام Capacitor
echo -e "\n${CYAN}[2/3] جاري مزامنة الملفات مع مشروع الأندرويد (Capacitor Sync)...${NC}"
npx cap sync
if [ $? -eq 0 ]; then
    echo -e "${GREEN}✓ تمت مزامنة الملفات والأصول بنجاح إلى داخل تطبيق الأندرويد!${NC}"
else
    echo -e "${RED}✗ فشلت مزامنة Capacitor. يرجى التحقق من الأخطاء.${NC}"
    exit 1
fi

# الخطوة 3: التحقق من إمكانية التجميع كـ APK وجاهزية المشروع
echo -e "\n${CYAN}[3/3] تهيئة ملفات Gradle وتجهيز تطبيق الأندرويد...${NC}"
echo -e "\n${GREEN}======================================================"
echo -e "🎉 تم تجهيز مشروع الأندرويد بنجاح ومزامنته بملفات الويب! 🎉"
echo -e "======================================================${NC}"
echo -e "\n${GOLD}💡 كيف يمكنك تجربة وبناء التطبيق على هاتفك الآن؟${NC}"
echo -e "1. افتح مجلد ${CYAN}'android'${NC} مباشرة باستخدام ${GOLD}Android Studio${NC} أو بتشغيل npx cap open android."
echo -e "2. سيقوم Android Studio بمزامنة ملفات Gradle تلقائياً ودون أي أخطاء."
echo -e "3. اضغط على زر ${GREEN}'Run' (تشغيل)${NC} لتثبيت التطبيق مباشرة على هاتفك أو المحاكي."
echo -e "4. لإنشاء نسخة Release APK جاهزة للنشر، اختر من القائمة العليا:"
echo -e "   ${GOLD}Build -> Build Bundle(s) / APK(s) -> Build APK(s)${NC}"
echo -e "   وستجد ملف الـ APK النهائي جاهزاً للتثبيت فوراً!"
echo -e "======================================================\n"
